from __future__ import annotations

from app.shared.models.domain import Product


class ProductCatalog:
    def __init__(self, seed_products: list[Product] | None = None) -> None:
        self._products = seed_products or self._build_seed_products()

    async def list_products(self) -> list[Product]:
        return [product.model_copy(deep=True) for product in self._products]

    async def get_candidate_products(self, limit: int = 20) -> list[Product]:
        products = await self.list_products()
        products.sort(key=lambda product: (-product.score, product.price))
        return products[:limit]

    def get_fallback_products(self, limit: int = 10) -> list[Product]:
        products = [product.model_copy(deep=True) for product in self._products]
        products.sort(key=lambda product: (-product.score, product.price))
        return products[:limit]

